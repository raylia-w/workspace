package DTO;

public class ShowPostDTO {

   private int p_no;
   private int u_no;
   private String id;
   private String p_name;
   private String p_main;
   private String p_date;
   private int p_score;
   
   public int getP_no() {
      return p_no;
   }
   public void setP_no(int p_no) {
      this.p_no = p_no;
   }
   public int getU_no() {
      return u_no;
   }
   public void setU_no(int u_no) {
      this.u_no = u_no;
   }
   public String getId() {
      return id;
   }
   public void setId(String id) {
      this.id = id;
   }
   public String getP_name() {
      return p_name;
   }
   public void setP_name(String p_name) {
      this.p_name = p_name;
   }
   public String getP_main() {
      return p_main;
   }
   public void setP_main(String p_main) {
      this.p_main = p_main;
   }
   public String getP_date() {
      return p_date;
   }
   public void setP_date(String p_date) {
      this.p_date = p_date;
   }
   public int getP_score() {
      return p_score;
   }
   public void setP_score(int p_score) {
      this.p_score = p_score;
   }

}